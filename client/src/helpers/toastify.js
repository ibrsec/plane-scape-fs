import { Zoom, toast } from "react-toastify";
import 'react-toastify/dist/ReactToastify.css';

export const toastSuccess = (msg)=> {
    toast.success( msg, {
        position: "bottom-right",
        autoClose: 4000,
        hideProgressBar: true,
        closeOnClick: true,
        pauseOnHover: true,
        draggable: true,
        progress: undefined,
        theme: "light",
        transition: Zoom,
        });
}
